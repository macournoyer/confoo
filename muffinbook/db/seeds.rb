Recipe.create :title => "Low Fat Bran and Fruit Muffins",
              :description => <<-EOS
These muffins are low-fat and high in fibre and are great warm right from the oven!

Ingredients
1/4 cup hot water
2 tablespoons whole or milled flax seeds
2 cups 100% bran cereal (not flakes)
3/4 cup low-fat milk (skim or 1%)
1 cup unsweetened applesauce
1/2 cup brown sugar
1/2 teaspoon salt
2 tablespoons oil
1 large egg
1 1/2 cups unbleached all-purpose flour
1 tablespoon baking powder
1/2 cup raisins
1/2 cup dried apricots, chopped
1/2 cup shredded carrot (1 medium carrot)

Method
Pour the hot water over the flax seeds. (If you're using whole flax seeds, whirl the mixture in a small grinder or food processor.)

Pour the bran cereal into a medium-sized mixing bowl; pour the flax mixture, milk and applesauce over the cereal. Let the mixture sit to absorb the liquid for several minutes.
Whisk in the brown sugar, salt, oil, and egg. In a separate bowl, whisk together the flour, baking powder and fruits. Gently mix these dry ingredients into the wet ingredients, stirring just until everything is well-combined.

Use a 1/4-cup measuring cup or muffin scoop to divide the batter evenly among 12 well-greased or paper-lined muffin cups; non-stick cooking spray works well here. The cups will be filled and slightly heaped above the rim of the muffin tin.

Bake the muffins in a preheated 400°F oven for 20 minutes, or until the middle of a muffin springs back when touched lightly with your finger. Let them cool for 5 minutes in the pan (this will make them easier to remove), then turn them out of the pan onto a rack to cool. Yield: 12 muffins.

Nutrition information per serving (1 muffin, 101g): 200 cal, 4g fat
EOS

Recipe.create :title => "Banana Oat Muffins",
              :description => <<-EOS
ingredients
2 very ripe bananas, mashed
1 cup brown sugar
1/4 cup canola or vegetable oil
2 eggs, lightly beaten
1 cup whole wheat flour
1 cup all purpose flour
1 1/2 tsp. baking powder
1/2 cup rolled oats
1/2 tsp. cinnamon
1/4 tsp. nutmeg
1/2 cup golden or sultana raisins

method
Preheat oven to 350° F.

In a medium bowl, combine bananas and brown sugar. Add oil and eggs and mix until smooth.

In a large bowl, combine both flours, baking powder, oats, cinnamon, and nutmeg.Stir to mix thoroughly. Add the wet ingredients to the dry ingredients and stir until just until incorporated. Fold in the raisins.

Line your muffin pan with paper liners and distribute the batter evenly. Bake for approximately 20 minutes or until a toothpick inserted in the centre comes out clean.
EOS

Recipe.create :title => "Chocolate Chocolate Chip Muffins",
              :description => <<-EOS
ingredients
2 eggs
1/2 cup oil
1 cup milk 
1 tsp. vanilla
1 3/4 cups all-purpose flour 
1/2 cup sugar
1/4 cup cocoa
1 Tbsp. baking powder
1/2 tsp. salt
1/2 cup semi-sweet chocolate chips

method
Preheat oven to 400° F. and grease muffin pan or line with paper liners.

In large bowl add and combine eggs, oil, milk and vanilla.

In smaller bowl combine well flour, sugar, cocoa, baking powder, salt and chocolate chips.

Combine wet and dry mixtures and fold together just until mixed.

Spoon into prepared pan. Bake for 20 minutes. Remove from pan and cool on rack.
